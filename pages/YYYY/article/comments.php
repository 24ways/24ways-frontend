                        <p>Hooray, a new year! Nice article, good to see non-frameworked JavaScript :-)</p>
                        <p>One regex gotcha:</p>
                        <ul>
                            <li>&#8220;.&#8221; means any character except newline, and w is alphanumeric+underscore – so if your source src attribute was &#8220;my-cat.mp4&#8221;, then the first line that sets the src attribute would end up putting in &#8220;bumper-in.cat&#8221;. If you don&#8217;t know what your filenames will be apart from the extension, plus you only care about the extension, probably better just to use something like /.(w+)$/</li>
                        </ul>
                        <p>And a couple of regex typos:</p>
                        <ul>
                            <li>The &#8220;w&#8221; character classes all appear to be missing their &#8220;&#8221;, unless you only want to match wwwww.www ;)</li>
                            <li>The last regex has &#8220;([w]+)&#8221; which also should presumably be w+</li>
                        </ul>
                        <p>Hope that&#8217;s helpful.</p>
